package Logical_pogram;

public class swaptwo_num 
{
public static void main(String[] args) 
{
	int a=10;                  //swap these num a=20,b=10
	int b=20;
	a=a+b;
	b=a-b;//30-20-----b=10
	a=a-b;//----------20
	System.out.println("after swaping num a:="+a);
	System.out.println("after swaping num b:="+b);
	
}
}
