package starpattern;

public class pattren4 
{
//*****
//****
//***
//**
//*
	public static void main(String[] args) 
	{
		int star=5;
		for(int i=5;i>=1;i--) 
		{
			for(int j=star;j>=1;j--) 
			{
				System.out.print("*");
			}
			System.out.println();
			star--;
		}
		
	}
	
}
